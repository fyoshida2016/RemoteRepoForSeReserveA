package model;

public class Room {
	private int rid;
	private String name;
	private String buildingName;
	private String floor;
	private String roomNumber;
	private int area;
	private int capacity;
	private String tel;
	private int whiteboard;
	private int projecter;
	private int screen;
	private int hdmi;
	private int dsub;
	private int composite;
	private int stereomini;

	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBuildingName() {
		return buildingName;
	}
	public void setBuildingName(String building) {
		this.buildingName = building;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	public String getRoomNumber() {
		return roomNumber;
	}
	public void setRoomNumber(String roomNumber) {
		this.roomNumber = roomNumber;
	}
	public int getArea() {
		return area;
	}
	public void setArea(int area) {
		this.area = area;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public int getWhiteboard() {
		return whiteboard;
	}
	public void setWhiteboard(int whiteboard) {
		this.whiteboard = whiteboard;
	}
	public int getProjecter() {
		return projecter;
	}
	public void setProjecter(int projecter) {
		this.projecter = projecter;
	}
	public int getScreen() {
		return screen;
	}
	public void setScreen(int screen) {
		this.screen = screen;
	}
	public int getHdmi() {
		return hdmi;
	}
	public void setHdmi(int hdmi) {
		this.hdmi = hdmi;
	}
	public int getDsub() {
		return dsub;
	}
	public void setDsub(int dsub) {
		this.dsub = dsub;
	}
	public int getComposite() {
		return composite;
	}
	public void setComposite(int composite) {
		this.composite = composite;
	}
	public int getStereomini() {
		return stereomini;
	}
	public void setStereomini(int stereomini) {
		this.stereomini = stereomini;
	}


}
